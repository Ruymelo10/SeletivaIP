class Carro:

    def __init__(self, cor, ano, km, cambio):
        self.cor = cor
        self.ano = ano
        self.km = km
        self.cambio = cambio

    def getCor(self):
        return self.cor

    def getAno(self):
        return self.ano

    def getKm(self):
        return self.km

    def getCambio(self):
        return self.cambio


